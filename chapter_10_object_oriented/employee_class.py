# Chapter 10: Object Oriented Programming
# Simple class and objects example

class Employee:
    name = "Harry"
    language = "Python"

# First object
harry = Employee()
harry.name = "Bhoomi"
print(harry.name, harry.language)

# Second object
rohan = Employee()
rohan.name = "Roha_n"
print(rohan.name, rohan.language)
